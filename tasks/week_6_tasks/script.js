// What is the MERN stack?
// The MERN stack is a collection of JavaScript technologies used to build web applications. It consists of four main components:
// 1. MongoDB: A NoSQL database that stores data in a flexible, JSON-like format.
// 2. Express: A web application framework for Node.js that provides a set of features for building web applications and APIs.
// 3. React: A JavaScript library for building user interfaces.
// 4. Node.js: A JavaScript runtime built on Chrome's V8 JavaScript engine.


// Compare javascript and PHP?
// JavaScript and PHP are both popular programming languages used for web development, but they have some key differences:
// 1. JavaScript is a client-side scripting language, meaning it runs in the browser, while PHP is a server-side scripting language, meaning it runs on the server.
// 2. JavaScript is used to create dynamic and interactive web pages, while PHP is used to generate dynamic content, interact with databases, and handle form submissions.
// 3. JavaScript is primarily used for front-end development, while PHP is used for back-end development.
// 4. JavaScript is a high-level, interpreted language, while PHP is a server-side scripting language that is compiled on the server.
// 5. JavaScript is commonly used with HTML and CSS to create web pages, while PHP is often used with MySQL to interact with databases.
// 6. JavaScript is supported by all modern web browsers, while PHP is supported by most web servers.
// 7. JavaScript is used for client-side validation, while PHP is used for server-side validation.
// 8. JavaScript is used for creating animations, games, and interactive elements on web pages, while PHP is used for creating dynamic web applications and websites.